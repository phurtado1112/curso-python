''' Secuencias de escape '''
## \n Salto de linea
## \t Tabulador
## \r Retorno de carro
## \' Comilla simple
## \" Comilla doble
## \\ Diagonal invertida

curso = 'Ultimate Python'

print(curso)
print('')

curso = 'Ultimate "Python"'

print(curso)
print('')

curso = "Ultimate \"Python\""

print(curso)
print('')

curso = 'Ultimate \n"Python"'

print(curso)
print('')