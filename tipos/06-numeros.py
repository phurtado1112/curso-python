""" Tipos de datos: Números """
numero = 2  # Entero
decimal = 1.2  # Decimal o flotante
imaginario = 2 + 3j  # Complejo o imaginario

print(5 + 2)  # Suma
print(5 - 2)  # Resta
print(5 * 2)  # Multiplicación
print(5 / 2)  # División
print(5 // 2)  # División entera
print(5 % 2)  # Módulo
print(5 ** 2)  # Potencia

numero = numero + 1  # Incremento
